class Command(BaseCommand):
    help = 'Загрузить начальные данные цен из кода в базу данных'

    def handle(self, *args, **options):
        created_cats = 0
        created_plans = 0

        for cat_data in PRICES_DATA:
            plans = cat_data.pop('plans')
            cat, created = PriceCategory.objects.get_or_create(
                key=cat_data['key'],
                defaults=cat_data,
            )
            if created:
                created_cats += 1

            for plan_data in plans:
                _, plan_created = PricePlan.objects.get_or_create(
                    category=cat,
                    name=plan_data['name'],
                    defaults=plan_data,
                )
                if plan_created:
                    created_plans += 1

        self.stdout.write(self.style.SUCCESS(
            f'Готово: создано категорий {created_cats}, тарифов {created_plans}.'
        ))
